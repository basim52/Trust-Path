export interface Lesson {
  id: string;
  title: string;
  duration: string;
  content: string; // Markdown or rich HTML-like formatted text
  exercise: {
    question: string;
    type: 'reframe' | 'selection' | 'writing';
    placeholder?: string;
    options?: string[];
    correctAnswer?: string;
    tip: string;
  };
}

export interface QuizQuestion {
  id: string;
  question: string;
  options: string[];
  correctAnswer: number; // Index of correct option
  explanation: string;
}

export interface Course {
  id: string;
  title: string;
  category: 'self-esteem' | 'confidence' | 'skills';
  description: string;
  icon: string; // Lucide icon name
  duration: string;
  lessonsCount: number;
  lessons: Lesson[];
  quiz: QuizQuestion[];
}

export interface TraineeProgress {
  completedCourses: string[]; // Course IDs
  completedLessons: string[]; // Lesson IDs ("courseId-lessonId")
  quizScores: Record<string, number>; // CourseID -> best score percentage
  certificates: Certificate[];
  dailyHabits: Record<string, boolean>; // HabitID -> completed today
  habitStreak: number;
  points: number;
  gratitudeJournal: JournalEntry[];
  thoughtReframings: ReframedThought[];
  assessments: Record<string, AssessmentResult>; // AssessmentType -> score & date
  completedParagraphs?: Record<string, boolean>; // Unique element key -> boolean
}

export interface Certificate {
  id: string;
  courseId: string;
  courseTitle: string;
  studentName: string;
  issuedAt: string;
  verificationCode: string;
}

export interface JournalEntry {
  id: string;
  date: string;
  items: string[]; // Three things grateful for
}

export interface ReframedThought {
  id: string;
  original: string;
  reframed: string;
  category: string;
  date: string;
}

export interface AssessmentResult {
  score: number;
  level: string;
  date: string;
  answers: Record<string, number>;
}

export interface Habit {
  id: string;
  title: string;
  category: string;
  points: number;
  icon: string;
}

